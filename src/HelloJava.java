// 파일명과 클래스 이름이 같아야 한다.
public class HelloJava { // 블록
    // 실행 시점
    // main 메서드 (메서드 vs 함수)
    // 자바는 main(String[] args) 메서드를 찾아서 프로그램을 실행한다.
    public static void main(String[] args) {
        // 값을 콘솔에 출력한다.
        // 자바는 한 문장이 끝날 때마다 세미콜론을 통해 구분한다.
        System.out.println("hello java");
    }
}

